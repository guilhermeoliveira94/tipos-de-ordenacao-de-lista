lista = ['Cláudio', 'José', 'Maria', 'Beltrano', 'João', 'Fulano', 'Ciclano']
print("Lista original: \n",lista)

lista.reverse()
print("Lista invertida: \n",lista)

lista.sort()
print("Lista ordenada em ordem Ascendente: \n",lista)

lista.sort(reverse=True)
print("Lista ordenada em ordem Descendete: \n",lista)